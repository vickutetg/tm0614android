
public class Product {
	private int id;
	private String name;
	private int price;
	private int amount;
	
	public void setId(int id){
		this.id = id;
	}
	public int getId(){
		return id;
	}
	
	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return name;
	}
	
	public void setPrice(int price){
		this.price = price;
	}
	public int getPrice(){
		return price;
	}
	
	public void setAmount(int amount){
		this.amount = amount;
	}
	public int getAmount(){
		return amount;
	}
}
