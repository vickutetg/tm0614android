package model;

public class Movie {
	
	private int poster;
	private String name;
	
	public int getPoster() {
		return poster;
	}
	public void setPoster(int poster) {
		this.poster = poster;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
